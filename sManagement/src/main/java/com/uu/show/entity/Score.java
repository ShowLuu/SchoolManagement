package com.uu.show.entity;

public class Score {

	public String scoreID;
	public String scoreStuID;
	public String scoreStuName;
	public String scoreSubID;
	public String scoreSubName;
	public String scoreSubScore;
	public String scoreTime;
	public String scoreRemark;

	public String getScoreID() {
		return scoreID;
	}

	public void setScoreID(String scoreID) {
		this.scoreID = scoreID;
	}

	public String getScoreStuID() {
		return scoreStuID;
	}

	public void setScoreStuID(String scoreStuID) {
		this.scoreStuID = scoreStuID;
	}

	public String getScoreStuName() {
		return scoreStuName;
	}

	public void setScoreStuName(String scoreStuName) {
		this.scoreStuName = scoreStuName;
	}

	public String getScoreSubID() {
		return scoreSubID;
	}

	public void setScoreSubID(String scoreSubID) {
		this.scoreSubID = scoreSubID;
	}

	public String getScoreSubName() {
		return scoreSubName;
	}

	public void setScoreSubName(String scoreSubName) {
		this.scoreSubName = scoreSubName;
	}

	public String getScoreTime() {
		return scoreTime;
	}

	public void setScoreTime(String scoreTime) {
		this.scoreTime = scoreTime;
	}

	public String getScoreRemark() {
		return scoreRemark;
	}

	public void setScoreRemark(String scoreRemark) {
		this.scoreRemark = scoreRemark;
	}

	public String getScoreSubScore() {
		return scoreSubScore;
	}

	public void setScoreSubScore(String scoreSubScore) {
		this.scoreSubScore = scoreSubScore;
	}

}
