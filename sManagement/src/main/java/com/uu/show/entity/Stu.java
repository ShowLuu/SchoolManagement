package com.uu.show.entity;

public class Stu {

	public String stuID;
	public String stuName;
	public String stuSex;
	public String stuTime;
	public String stuBirth;
	public String stuPhone;
	public String stuAddr;
	public String stuCls;

	public String getStuID() {
		return stuID;
	}

	public void setStuID(String stuID) {
		this.stuID = stuID;
	}

	public String getStuName() {
		return stuName;
	}

	public void setStuName(String stuName) {
		this.stuName = stuName;
	}

	public String getStuSex() {
		return stuSex;
	}

	public void setStuSex(String stuSex) {
		this.stuSex = stuSex;
	}

	public String getStuTime() {
		return stuTime;
	}

	public void setStuTime(String stuTime) {
		this.stuTime = stuTime;
	}

	public String getStuBirth() {
		return stuBirth;
	}

	public void setStuBirth(String stuBirth) {
		this.stuBirth = stuBirth;
	}

	public String getStuPhone() {
		return stuPhone;
	}

	public void setStuPhone(String stuPhone) {
		this.stuPhone = stuPhone;
	}

	public String getStuAddr() {
		return stuAddr;
	}

	public void setStuAddr(String stuAddr) {
		this.stuAddr = stuAddr;
	}

	public String getStuCls() {
		return stuCls;
	}

	public void setStuCls(String stuCls) {
		this.stuCls = stuCls;
	}

}
